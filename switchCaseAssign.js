function monYear(monDay) {
  switch (monDay) {
    case 1:
      console.log(`Month number ${monDay} :"January" `);

      break;
    case 2:
      console.log(`Month number ${monDay} :"February" `);

      break;
    case 3:
      console.log(`Month number ${monDay} :"March" `);

      break;
    case 4:
      console.log(`Month number ${monDay} :"April" `);

      break;
    case 5:
      console.log(`Month number ${monDay} :"May" `);

      break;
    case 6:
      console.log(`Month number ${monDay} :"June" `);

      break;
    case 7:
      console.log(`Month number ${monDay} :"July" `);

      break;
    case 8:
      console.log(`Month number ${monDay} :"August" `);

      break;
    case 9:
      console.log(`Month number ${monDay} :"September" `);

      break;
    case 10:
      console.log(`Month number ${monDay} :"October" `);

      break;
    case 11:
      console.log(`Month number ${monDay} :"November" `);

      break;
    case 12:
      console.log(`Month number ${monDay} :"December" `);

      break;

    default:
      console.log(`Invalid input => ${monDay}`);
      break;
  }
}
